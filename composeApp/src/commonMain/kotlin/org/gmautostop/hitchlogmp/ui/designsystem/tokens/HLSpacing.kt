package org.gmautostop.hitchlogmp.ui.designsystem.tokens

import androidx.compose.ui.unit.dp

/**
 * Spacing scale for HitchLog application.
 * Provides consistent spacing values across all components.
 * Based on 4dp grid system.
 */
object HLSpacing {
    val xs = 4.dp      // Extra small - minimal spacing
    val sm = 6.dp      // Small - tight spacing
    val md = 8.dp      // Medium - default spacing
    val lg = 12.dp     // Large - comfortable spacing
    val xl = 16.dp     // Extra large - section spacing
    val xxl = 24.dp    // 2X large - major section spacing
    val xxxl = 32.dp   // 3X large - screen padding
}
